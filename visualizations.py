"""## Chart 4 — Model Comparison Grouped Bar Chart"""

metric_cols = ['Accuracy', 'Precision', 'Recall', 'F1-Score']
models      = metrics['Model'].tolist()
x           = np.arange(len(metric_cols))
n_models    = len(models)
bar_width   = 0.22
bar_colors  = ['#3498db', '#e74c3c', '#2ecc71', '#9b59b6']

fig, ax = plt.subplots(figsize=(14, 7))

for i, (model, color) in enumerate(zip(models, bar_colors)):
    values = metrics[metrics['Model'] == model][metric_cols].values[0]
    offset = (i - n_models/2 + 0.5) * bar_width
    bars = ax.bar(x + offset, values, bar_width,
                  label=model, color=color, edgecolor='white', linewidth=0.8)
    for bar, val in zip(bars, values):
        ax.text(bar.get_x() + bar.get_width()/2,
                bar.get_height() + 0.005,
                f'{val:.2f}',
                ha='center', va='bottom', fontsize=8.5, fontweight='bold')

ax.set_xticks(x)
ax.set_xticklabels(metric_cols, fontsize=12)
ax.set_ylabel('Score', fontsize=12)
ax.set_title('Chart 4 — Model Comparison Across All Metrics', fontsize=14, fontweight='bold')
ax.set_ylim(0, 1.12)
ax.legend(fontsize=10, loc='lower right')
ax.spines[['top','right']].set_visible(False)
ax.yaxis.grid(True, linestyle='--', alpha=0.5)
ax.set_axisbelow(True)

plt.tight_layout()
plt.savefig('visuals/chart4_model_comparison.png', dpi=300, bbox_inches='tight')
