"""## Chart 3 — Confusion Matrix Heatmap (per model)"""

def plot_confusion(ax_raw, ax_norm, cm, title):
    cm_norm = cm.astype('float') / cm.sum(axis=1, keepdims=True) * 100

    sns.heatmap(cm, annot=True, fmt='d', cmap='Blues',
                xticklabels=CLASS_NAMES, yticklabels=CLASS_NAMES,
                linewidths=0.5, ax=ax_raw, cbar=False)
    ax_raw.set_title(f'{title}\n(Raw Counts)', fontsize=11, fontweight='bold')
    ax_raw.set_xlabel('Predicted Label', fontsize=10)
    ax_raw.set_ylabel('Actual Label', fontsize=10)

    sns.heatmap(cm_norm, annot=True, fmt='.1f', cmap='Reds',
                xticklabels=CLASS_NAMES, yticklabels=CLASS_NAMES,
                linewidths=0.5, ax=ax_norm, cbar=False)
    ax_norm.set_title(f'{title}\n(Row-Normalised %)', fontsize=11, fontweight='bold')
    ax_norm.set_xlabel('Predicted Label', fontsize=10)
    ax_norm.set_ylabel('Actual Label', fontsize=10)

models_cm = [
    (cm_nb,   'Naive Bayes (TF-IDF)'),
    (cm_lr,   'Logistic Regression (TF-IDF)'),
    (cm_w2v,  'Logistic Regression (Word2Vec)'),
]

fig, axes = plt.subplots(3, 2, figsize=(12, 16))
fig.suptitle('Chart 3 — Confusion Matrices (All Models)', fontsize=15, fontweight='bold')

for row, (cm, title) in enumerate(models_cm):
    plot_confusion(axes[row][0], axes[row][1], cm, title)

plt.tight_layout()
plt.savefig('visuals/chart3_confusion_matrices.png', dpi=300, bbox_inches='tight')
plt.show()
